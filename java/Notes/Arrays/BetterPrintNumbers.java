public class BetterPrintNumbers {
    public static void main(String[] args)
    {
        int i;
        int[] numbers = {12, 34, 45, 56, 67, 78};

        System.out.println("\nThe values in the array are:-\n");
        for(i = 0; i < 5; i++)
        {
            System.out.println(numbers[i]);
        }
    }
}
